/* ─── i18n engine ──────────────────────────────────────────────── */
(function () {
  const STORAGE_KEY = 'innoventum_lang';
  const LANGS = ['fr', 'en', 'sv'];

  // Detect initial language: stored preference → browser lang → fr
  let lang = localStorage.getItem(STORAGE_KEY);
  if (!lang) {
    const browser = (navigator.language || 'fr').slice(0, 2).toLowerCase();
    lang = LANGS.includes(browser) ? browser : 'fr';
  }

  function t(key) {
    return (TRANSLATIONS[lang] || {})[key] || (TRANSLATIONS.fr || {})[key] || key;
  }

  function apply() {
    document.documentElement.lang = lang;
    document.title = t('page.title');

    // Plain text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const v = t(el.getAttribute('data-i18n'));
      if (v) el.textContent = v;
    });

    // Inner HTML (translations containing <em>, <br>, &amp; etc.)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const v = t(el.getAttribute('data-i18n-html'));
      if (v) el.innerHTML = v;
    });

    // Input / textarea placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const v = t(el.getAttribute('data-i18n-ph'));
      if (v) el.placeholder = v;
    });

    // Update switcher active state
    document.querySelectorAll('[data-lang]').forEach(btn => {
      const active = btn.dataset.lang === lang;
      btn.classList.toggle('active', active);
      btn.setAttribute('aria-pressed', active ? 'true' : 'false');
    });
  }

  function setLang(l) {
    if (!LANGS.includes(l)) return;
    lang = l;
    localStorage.setItem(STORAGE_KEY, l);
    apply();
  }

  function init() {
    document.querySelectorAll('[data-lang]').forEach(btn => {
      btn.addEventListener('click', () => setLang(btn.dataset.lang));
    });
    apply();
  }

  document.readyState === 'loading'
    ? document.addEventListener('DOMContentLoaded', init)
    : init();
})();
